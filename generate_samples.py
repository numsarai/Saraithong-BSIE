"""
generate_samples.py
-------------------
Generate 10 realistic Thai bank statement sample files with different formats,
column structures, and data patterns to test BSIE pipeline robustness.

Each file simulates a real bank's export style:
  1. KBANK  – dual_account, Thai columns, debit/credit, K PLUS channel
  2. SCB    – dual_account, Thai columns, header on row 2, SCB EASY channel
  3. KTB    – ktb_transfer, unsigned amount, sender/receiver comparison
  4. BBL    – standard, signed amount single column, English+Thai mixed
  5. BAY    – standard, debit/credit columns, Krungsri mobile
  6. TTB    – standard, English columns, PromptPay transfers
  7. GSB    – standard, Thai only columns, passbook style with running balance
  8. KBANK_EN – KBANK English export variant
  9. SCB_SAVINGS – SCB savings account with CDM/ATM cash transactions
 10. MIXED  – Messy real-world file: merged header rows, extra blank rows, notes
"""

import os
import sys
from pathlib import Path

import openpyxl
from openpyxl.styles import Font, Alignment, PatternFill

OUT_DIR = Path(__file__).parent / "data" / "sample_statements"
OUT_DIR.mkdir(parents=True, exist_ok=True)

# ── Helpers ───────────────────────────────────────────────────────────────

def _wb(sheet_name="Sheet1"):
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = sheet_name
    return wb, ws

def _save(wb, name):
    p = OUT_DIR / name
    wb.save(str(p))
    print(f"  Created: {p.name}")
    return p


